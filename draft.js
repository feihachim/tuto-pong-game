"use strict";

class Ball {
    constructor(x, y, radius, speed, velocityX, velocityY, color) {
        this.x = x;
        this.y = y;
        this.radius = radius;
        this.speed = speed;
        this.velocityX = velocityX;
        this.velocityY = velocityY;
        this.color = color;
    }


}